//Relational Operators

console.log(100>20);
console.log(100<20);
console.log(10>=10);
console.log(20<=30);

console.log(10==10);
console.log('100' == 100); //ignore the type -> true
console.log('100' === 100); // strict equal does include the data type -> false

//!= : not equal
console.log(100 !='100'); // false
//!== strict not equal
// Number does not equal to String -> true
console.log(100 !=='100'); 


